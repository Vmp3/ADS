DELIMITER $$

CREATE PROCEDURE CalcularQuintoDiaUtil(IN ano_param INT, IN mes_param INT, OUT resultado DATE)
BEGIN

    DECLARE primeiroDiaUtil DATE;
    DECLARE quintoDiaUtil DATE;
    

    SET primeiroDiaUtil = DATE_ADD(DATE_FORMAT(CONCAT(ano_param, '-', mes_param, '-01'), '%Y-%m-%d'), INTERVAL 1 MONTH);
    

    SET quintoDiaUtil = primeiroDiaUtil;
    SET @contador_diasUteis = 0;
    

    WHILE @contador_diasUteis < 5 DO
        SET quintoDiaUtil = DATE_ADD(quintoDiaUtil, INTERVAL 1 DAY);
        

        IF DAYOFWEEK(quintoDiaUtil) NOT IN (1, 7) THEN
            SET @contador_diasUteis = @contador_diasUteis + 1;
        END IF;
    END WHILE;
    

    SET resultado = quintoDiaUtil;
END $$

CREATE PROCEDURE GerarRegistrosPagamento(IN ano_param INT, IN mes_param INT)
BEGIN
    DECLARE quintoDiaUtil DATE;


    CALL CalcularQuintoDiaUtil(ano_param, mes_param, quintoDiaUtil);


    DELETE FROM FOLHA_PAGAMENTO
    WHERE YEAR(DT_PAGAMENTO) = ano_param AND MONTH(DT_PAGAMENTO) = mes_param;

	INSERT INTO FOLHA_PAGAMENTO (IDFUNCIONARIO, ANO_REFERENCIA, MES_REFERENCIA, DT_GERACAO, DT_PAGAMENTO, VALOR_BASE, AUXILIO_MEDICO, AUXILIO_TRANSPORTE, AUXILIO_ALIMENTACAO, BONUS_FUNCAO)
	SELECT
		IDFUNCIONARIO,
		ano_param AS ANO_REFERENCIA,
		mes_param AS MES_REFERENCIA,
		CAST(NOW() AS DATE) AS DT_GERACAO,
		quintoDiaUtil AS DT_PAGAMENTO,
		SALARIO_BASE,
		0.0 AS AUXILIO_MEDICO,
		0.0 AS AUXILIO_TRANSPORTE,
		0.0 AS AUXILIO_ALIMENTACAO,
		CAST((SALARIO_BASE * (SELECT BONUS FROM FUNCAO WHERE IDFUNCAO = FUNCIONARIO.IDFUNCAO) / 100) AS DECIMAL(10, 2)) AS BONUS_FUNCAO
	FROM FUNCIONARIO;
END $$

